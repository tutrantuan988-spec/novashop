export function getCategoryMeta(slug) {
  return CATEGORY_META[slug] || null;
}

export function getProductsByCategorySlug(slug) {
  const meta = getCategoryMeta(slug);
  return meta ? meta.products : [];
}

export function getAllBrands(slug) {
  const products = getProductsByCategorySlug(slug);
  return [...new Set(products.map((p) => p.brand))];
}

export function getPriceRange(slug) {
  const products = getProductsByCategorySlug(slug);
  if (!products.length) return { min: 0, max: 0 };
  const prices = products.map((p) => p.price);
  return { min: Math.min(...prices), max: Math.max(...prices) };
}

export function filterProducts(slug, filters) {
  let products = getProductsByCategorySlug(slug);
  if (!products) return [];

  if (filters.brand && filters.brand !== 'all') {
    products = products.filter((p) => p.brand === filters.brand);
  }

  if (filters.query) {
    const q = filters.query.toLowerCase();
    products = products.filter(
      (p) =>
        p.name.toLowerCase().includes(q) ||
        p.description.toLowerCase().includes(q) ||
        p.brand.toLowerCase().includes(q)
    );
  }

  if (filters.minPrice !== undefined || filters.maxPrice !== undefined) {
    products = products.filter((p) => {
      const min = filters.minPrice ?? 0;
      const max = filters.maxPrice ?? Infinity;
      return p.price >= min && p.price <= max;
    });
  }

  if (filters.badge) {
    products = products.filter((p) => p.badge === filters.badge);
  }

  switch (filters.sort) {
    case 'price-asc':
      products.sort((a, b) => a.price - b.price);
      break;
    case 'price-desc':
      products.sort((a, b) => b.price - a.price);
      break;
    case 'rating':
      products.sort((a, b) => b.rating - a.rating);
      break;
    case 'popular':
      products.sort((a, b) => b.reviewCount - a.reviewCount);
      break;
    default:
      break;
  }

  return products;
}

